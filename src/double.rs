#[allow(unused)]
#[allow(non_camel_case_types)]
#[allow(non_snake_case)]
#[derive(Clone, Copy)]
#[repr(C, packed)]
#[bitfield]
struct Ieee {
    /* Together these comprise the mantissa.  */
    #[allow(dead_code)]
    pub mantissa1: u32,
    #[allow(dead_code)]
    pub mantissa0: B20,
    #[allow(dead_code)]
    pub exponent: B11,
    #[allow(dead_code)]
    pub negative: B1,
}
sa::const_assert_eq!(8, mem::size_of::<Ieee>());

#[allow(non_camel_case_types)]
#[allow(non_snake_case)]
#[derive(Clone, Copy)]
#[repr(C/*, packed*/)]
#[repr(align(8))]
union ieee754_double {
    pub d: f64,
    pub ieee: Ieee,
    pub u: u64,
    pub bytes: [u8; 8],
    pub ba: BitArray<[u64; 1]>,
    pub ul: [u32; 2],
}
sa::const_assert_eq!(8, mem::size_of::<ieee754_double>());
sa::const_assert_eq!(8, mem::align_of::<ieee754_double>());

