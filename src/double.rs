/// Coding of a frequent double or exponent value.
#[allow(non_camel_case_types)]
#[allow(non_snake_case)]
#[derive(Default, Clone, Copy)]
struct CodingOfFrequentDoubleOrExponent {
    pub Type: ValueType,
    pub NumBits: i16,
    pub Bits: u32,
    pub s: ieee754_double,
}
type C = CodingOfFrequentDoubleOrExponent;
impl Ord for CodingOfFrequentDoubleOrExponent {
    fn cmp(&self, other: &Self) -> Ordering {
        let lhs: ieee754_double = ieee754_double {
            d: unsafe { self.s.d },
        };
        let rhs: ieee754_double = ieee754_double {
            d: unsafe { other.s.d },
        };
        let left_exp = unsafe { lhs.ieee.exponent() };
        let right_exp = unsafe { rhs.ieee.exponent() };
        left_exp.cmp(&right_exp)
    }
}
impl PartialOrd for CodingOfFrequentDoubleOrExponent {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        Some(self.cmp(other))
    }
}
impl PartialEq for CodingOfFrequentDoubleOrExponent {
    fn eq(&self, other: &Self) -> bool {
        let lhs: ieee754_double = ieee754_double {
            d: unsafe { self.s.d },
        };
        let rhs: ieee754_double = ieee754_double {
            d: unsafe { other.s.d },
        };
        let left_exp = unsafe { lhs.ieee.exponent() };
        let right_exp = unsafe { rhs.ieee.exponent() };
        left_exp == right_exp
    }
}
impl Eq for CodingOfFrequentDoubleOrExponent {}

/// DOUBLEWITHTWODWORD in spec
#[allow(non_snake_case)]
macro_rules! D {
    ( $l:expr, $r:expr ) => {{
        let tmp: ieee754_double = ieee754_double { ul: [$r, $l] };
        tmp
    }};
}

#[allow(non_snake_case)]
macro_rules! XX {
    ( $t:expr, $n:expr, $b:expr, $l:expr, $r:expr ) => {{
        let a: C = C {
            Type: $t,
            NumBits: $n,
            Bits: $b,
            s: ieee754_double { ul: [$r, $l] },
        };
        a
    }};
}

fn getcofdoe<'a>(bits: u32, nbits: i16) -> Option<&'a CodingOfFrequentDoubleOrExponent> {
    for i in 0..N {
        if ACOFDOE[i].NumBits == nbits && ACOFDOE[i].Bits == bits {
            return Some(&ACOFDOE[i]);
        }
    }
    None
}

pub fn read_double_from_reader<R: BitRead>(rdr: &mut R) -> io::Result<f64> {
    let mut ucofdoe: u32 = 0;
    let mut value: ieee754_double = ieee754_double { d: 0.0 };
    let mut found: bool = false;
    let mut code: C = Default::default();
    for i in 1..22 + 1 {
        ucofdoe = ucofdoe << 1;
        //ucofdoe |= Boolean::from_reader(rdr)?.value as u32;
        //let b: bool = rdr.read_bit()?;
        let b = read_bits(rdr, 1)?;
        ucofdoe |= b as u32;
        match getcofdoe(ucofdoe, i as i16) {
            None => {
                continue;
            }
            Some(c) => {
                unsafe {
                    value.d = c.s.d;
                }
                code = *c;
                found = true;
                break;
            }
        }
    }
    if !found {
        return Err(std::io::Error::new(
            std::io::ErrorKind::Other,
            "prc double: Coding pattern not found!",
        ));
    }

    //let d: f64 = unsafe { value.d };

    //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
    if code.NumBits == 2 && code.Bits == 1 && code.Type == VT::double {
        return Ok(unsafe { value.d });
    }

    unsafe {
        //value.ieee.set_negative(rdr.read_bit()? as u8);
        value.ieee.set_negative(read_bits(rdr, 1)? as u8);
    }
    //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );

    if code.Type == VT::double {
        return Ok(unsafe { value.d });
    }

    //let has_mantissa: bool = rdr.read_bit()?;
    let has_mantissa: bool = read_bits(rdr, 1)? != 0;
    if !has_mantissa {
        return Ok(unsafe { value.d });
    }

    //let b4: u8 = rdr.read::<4, u8>()? & 0x0F;
    let b4: u8 = read_bits(rdr, 4)? as u8 & 0x0F;
    //println!("u64={:064b} b4={:04b}", unsafe { value.u }, b4);
    unsafe {
        value.bytes[6] |= b4;
    }

    let mut lbi: i8 = 0; // index of last byte
    let mut cbi: i8 = 5; // index of current byte
    while cbi >= lbi {
        //let has_new_byte: bool = rdr.read_bit().unwrap();
        let has_new_byte: bool = read_bits(rdr, 1)? != 0;
        //println!("cbi={} lbi={} has_nb={}", cbi, lbi, has_new_byte);
        if has_new_byte {
            //let cb = UnsignedCharacter::from_reader(rdr)?.value as u8;
            //let cb: u8 = rdr.read_to().unwrap();
            let cb: u8 = read_bits(rdr, 8)?;
            unsafe {
                value.bytes[cbi as usize] = cb;
            }
            //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
        } else {
            //let offset: u8 = rdr.read::<3, u8>()? & 0x07;
            let mut offset: u8 = read_bits(rdr, 3)? & 0x07;
            // offset |= ((read_bits(rdr, 1)?&0x01)<<2);
            // offset |= ((read_bits(rdr, 1)?&0x01)<<1);
            // offset |= ((read_bits(rdr, 1)?&0x01)<<0);
            //println!("offset={}", offset);
            if offset == 0 {
                let b = unsafe { value.bytes[cbi as usize + 1] };
                //println!("b={:08b}", b);
                while cbi >= lbi {
                    unsafe {
                        value.bytes[cbi as usize] = b;
                    };
                    //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
                    cbi = cbi - 1;
                }
                break;
            } else if offset == 6 {
                let b = unsafe { value.bytes[cbi as usize + 1] };
                //println!("b={:08b}", b);
                lbi = lbi + 1;
                while cbi >= lbi {
                    //println!("cbi={} lbi={}", cbi, lbi);
                    unsafe {
                        value.bytes[cbi as usize] = b;
                    };
                    //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
                    cbi = cbi - 1;
                }
                //unsafe { value.bytes[cbi as usize] = UnsignedCharacter::from_reader(rdr)?.value as u8; };
                //let uc8: u8 = rdr.read_to()?;
                let uc8: u8 = read_bits(rdr, 8)?;
                unsafe {
                    value.bytes[cbi as usize] = uc8;
                };
                //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
                break;
            } else {
                //assert!((cbi + offset as i8) < 8);
                unsafe {
                    value.bytes[cbi as usize] = value.bytes[cbi as usize + offset as usize];
                }
                //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );
            }
        }
        cbi = cbi - 1;
    }
    //println!("u64={:064b} d={}", unsafe { value.u }, unsafe { value.d } );

    Ok(unsafe { value.d })
}

pub fn write_double_to_writer<W: BitWrite + ?Sized>(w: &mut W, d: f64) -> std::io::Result<u32> {
    let _value: ieee754_double = ieee754_double { d };
    //println!("{}", unsafe { value.u } );
    let mut cofdoe: CodingOfFrequentDoubleOrExponent = Default::default();
    cofdoe.s.d = d;
    let mut _pcofdoe: usize = ACOFDOE.binary_search(&cofdoe).unwrap();

    while _pcofdoe > 0
        && unsafe {
            ieee754_double {
                d: ACOFDOE[_pcofdoe].s.d,
            }
            .ieee
            .exponent()
                == ieee754_double {
                    d: ACOFDOE[_pcofdoe - 1].s.d,
                }
                .ieee
                .exponent()
        }
    {
        _pcofdoe -= 1;
    }

    while ACOFDOE[_pcofdoe].Type == ValueType::double {
        if d.abs() == unsafe { ACOFDOE[_pcofdoe].s.d } {
            break;
        }
        _pcofdoe += 1;
    }

    #[allow(unused)]
    let mut bits_written = 0;
    let pcofdoe = ACOFDOE[_pcofdoe];

    let from = 1 << pcofdoe.NumBits - 1;
    let mut i = from;
    while i >= 1 {
        //w.write_bit(pcofdoe.Bits & i != 0)?;
        write_bits(w, (pcofdoe.Bits & i != 0) as u8, 1)?;
        bits_written += 1;
        i = i >> 1;
    }

    #[allow(non_snake_case)]
    let stadwZero = D!(0x00000000, 0x00000000);
    #[allow(non_snake_case)]
    let stadwNegativeZero = D!(0x80000000, 0x00000000);

    if unsafe { _value.ul == stadwZero.ul || _value.ul == stadwNegativeZero.ul } {
        return Ok(bits_written);
    }

    //w.write_bit(unsafe { _value.ieee }.negative() != 0)?;
    write_bits(w, (unsafe { _value.ieee }.negative() != 0) as u8, 1)?;
    bits_written += 1;

    if pcofdoe.Type == ValueType::double {
        return Ok(bits_written);
    }

    if unsafe { _value.ieee.mantissa0() == 0 && _value.ieee.mantissa1() == 0 } {
        //w.write_bit(false)?;
        write_bits(w, 0, 1)?;
        bits_written += 1;
        return Ok(bits_written);
    }

    //let _ = w.write_bit(true)?;
    let _ = write_bits(w, 1, 1)?;
    bits_written += 1;

    let mut bi: usize = 6; // byte index same as pb
    let b4: u8 = unsafe { _value.bytes[bi] } & 0x0F;
    //let _ = w.write::<4, _>(b4)?;
    write_bits(w, b4, 4)?;
    bits_written += 4;

    bi -= 1;
    let start = bi;

    let end: usize = 0;
    let mut stop: usize = 0;
    #[allow(non_snake_case)]
    let mut bSaveAtEnd: u8 = 0;
    #[allow(non_snake_case)]
    let fSaveAtEnd: bool = unsafe { _value.bytes[0] != _value.bytes[1] };
    if fSaveAtEnd {
        bSaveAtEnd = unsafe { _value.bytes[0] };
    }
    stop += 1;

    fn memchr(bytes: &[u8; 8], start: usize, key: u8, n: usize) -> Option<usize> {
        let end = start + n;
        for i in start..end {
            assert!(i < bytes.len());
            if bytes[i] == key {
                assert_eq!(bytes[i], key);
                return Some(i);
            }
        }
        None
    }

    unsafe {
        while _value.bytes[stop] == _value.bytes[stop + 1] {
            stop += 1;
        }

        let mut result: usize = 0;
        while bi >= stop {
            let mut found: bool = false;
            if bi != start {
                match memchr(&_value.bytes, bi + 1, _value.bytes[bi], start - bi) {
                    Some(x) => {
                        result = x;
                        found = true
                    }
                    None => found = false,
                };
            }
            if bi != start && found {
                //let _ = w.write_bit(false)?;
                let _ = write_bits(w, 0, 1)?;
                bits_written += 1;
                let b3: u8 = (result - bi) as u8 & 0x07;
                //let _ = w.write::<3, _>(b3)?;
                let _ = write_bits(w, b3, 3)?;
                bits_written += 3;
            } else {
                //let _ = w.write_bit(true)?;
                let _ = write_bits(w, 1, 1)?;
                bits_written += 1;
                let byte: u8 = _value.bytes[bi];
                //let _ = w.write::<8, _>(byte)?;
                let _ = write_bits(w, byte, 8)?;
                bits_written += 8;
            }

            bi = bi - 1;
        }

        if !(end + 1 >= stop) {
            if fSaveAtEnd {
                //let _ = w.write_bit(false)?;
                let _ = write_bits(w, 0, 1)?;
                bits_written += 1;
                let b3: u8 = 6;
                //let _ = w.write::<3, _>(b3)?;
                let _ = write_bits(w, b3, 3)?;
                bits_written += 3;
                //let _ = w.write::<8, _>(bSaveAtEnd)?;
                let _ = write_bits(w, bSaveAtEnd, 8)?;
                bits_written += 8;
            } else {
                //let _ = w.write_bit(false)?;
                let _ = write_bits(w, 0, 1)?;
                bits_written += 1;
                //let _ = w.write::<3, _>(0u8)?;
                let _ = write_bits(w, 0, 3)?;
                bits_written += 3;
            }
        } else {
            let found: bool;
            match memchr(&_value.bytes, bi + 1, _value.bytes[bi], start - bi) {
                Some(x) => {
                    result = x;
                    found = true
                }
                None => found = false,
            };
            if found {
                //let _ = w.write_bit(false)?;
                let _ = write_bits(w, 0, 1)?;
                bits_written += 1;
                let b3 = (result - bi) as u8 & 0x07;
                //let _ = w.write::<3, _>(b3)?;
                let _ = write_bits(w, b3, 3)?;
                bits_written += 3;
            } else {
                //let _ = w.write_bit(true)?;
                let _ = write_bits(w, 1, 1)?;
                bits_written += 1;
                //let _ = w.write::<8, _>(_value.bytes[bi])?;
                let _ = write_bits(w, _value.bytes[bi], 8)?;
                bits_written += 8;
            }
        }
    }
    if bits_written > 71 {
        //println!("{} needed {} bits", d, bits_written);
    }
    //assert!(bits_written <= 64);

    Ok(bits_written)
}

